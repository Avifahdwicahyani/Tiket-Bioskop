    private void tblTiketMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTiketMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tblTiket.getModel();
        int row = tblTiket.getSelectedRow();
        
        Tiket tiket = new Tiket();
        
        tiket = tiket.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtIdTiket.setText(String.valueOf(tiket.getIdtiket()));
        cmbBus.getModel().setSelectedItem(tiket.getBus().getNamabus());
        txtJadwal.setText(tiket.getJadwal());
        txtNoKursi.setText(tiket.getNokursi());
        txtKelas.setText(tiket.getKelas());
    }//GEN-LAST:event_tblTiketMouseClicked

