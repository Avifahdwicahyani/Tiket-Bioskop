    public void tampilkanData() {
        String[] kolom = {"ID Tiket", "Bus", "Jadwal", "Nomor Kursi", "Kelas"};
        ArrayList<Tiket> list = new Tiket().getAll();
        Object rowData[] = new Object[5];
        
        tblTiket.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getIdtiket();
            rowData[1] = list.get(i).getBus().getNamabus();
            rowData[2] = list.get(i).getJadwal();
            rowData[3] = list.get(i).getNokursi();
            rowData[4] = list.get(i).getKelas();
            
            ((DefaultTableModel)tblTiket.getModel()).addRow(rowData);
        }
    }
    
    public void cari(String keyword) {
        String[] kolom = {"ID Tiket", "Bus", "Jadwal", "Nomor Kursi", "Kelas"};
        ArrayList<Tiket> list = new Tiket().search(keyword);
        Object rowData[] = new Object[5];
        
        tblTiket.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        
        for(int i = 0; i < list.size(); i++) {
            rowData[0] = list.get(i).getIdtiket();
            rowData[1] = list.get(i).getBus().getNamabus();
            rowData[2] = list.get(i).getJadwal();
            rowData[3] = list.get(i).getNokursi();
            rowData[4] = list.get(i).getKelas();
            
            ((DefaultTableModel)tblTiket.getModel()).addRow(rowData);
        }
    }
    
