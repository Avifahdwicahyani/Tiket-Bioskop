    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        // TODO add your handling code here:
        Tiket tiket = new Tiket();
        tiket.setIdtiket(Integer.parseInt(txtIdTiket.getText()));
        tiket.setBus((Bus)cmbBus.getSelectedItem());
        tiket.setJadwal(txtJadwal.getText());
        tiket.setNokursi(txtNoKursi.getText());
        tiket.setKelas(txtKelas.getText());
        tiket.save();
        
        txtIdTiket.setText(Integer.toString(tiket.getIdtiket()));
        
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

